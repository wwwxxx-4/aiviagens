// ─── Merge + Dedup ────────────────────────────────────────────────────────────
const ALL = [...BR, ...SA, ...NA, ...EU, ...INTL]
const seen = new Set<string>()
export const AIRPORTS_UNIQUE: Airport[] = ALL.filter(a => {
  const key = a.iata + '|' + a.city
  if (seen.has(key)) return false
  seen.add(key)
  return true
})

// ─── Search ───────────────────────────────────────────────────────────────────
function normalize(s: string) {
  return s.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '')
}

export function searchAirports(query: string, limit = 7): Airport[] {
  if (!query || query.length < 2) return []
  const q = normalize(query)
  // Exact IATA match first
  const exact = AIRPORTS_UNIQUE.filter(a => a.iata.toLowerCase() === q)
  // City/name starts with
  const starts = AIRPORTS_UNIQUE.filter(a =>
    a.iata.toLowerCase() !== q &&
    (normalize(a.city).startsWith(q) || normalize(a.name).startsWith(q))
  )
  // Contains anywhere
  const contains = AIRPORTS_UNIQUE.filter(a =>
    a.iata.toLowerCase() !== q &&
    !normalize(a.city).startsWith(q) &&
    !normalize(a.name).startsWith(q) &&
    (normalize(a.city).includes(q) || normalize(a.name).includes(q) ||
     normalize(a.country).includes(q) || (a.state ? normalize(a.state).includes(q) : false))
  )
  return [...exact, ...starts, ...contains].slice(0, limit)
}
